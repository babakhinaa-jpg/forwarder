const I18nContext = createContext(null);

function detectLang() {
  const stored = localStorage.getItem('pf_lang');
  if (stored && T[stored]) return stored;
  const browser = (navigator.language || 'en').split('-')[0];
  return T[browser] ? browser : 'en';
}

export function I18nProvider({ children }) {
  const [lang, setLangState] = useState(detectLang);

  const setLang = useCallback((l) => {
    localStorage.setItem('pf_lang', l);
    setLangState(l);
  }, []);

  const t = useCallback((key) => T[lang]?.[key] ?? T.en?.[key] ?? key, [lang]);

  return (
    <I18nContext.Provider value={{ lang, setLang, t, languages: LANGUAGES }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() { return useContext(I18nContext); }
